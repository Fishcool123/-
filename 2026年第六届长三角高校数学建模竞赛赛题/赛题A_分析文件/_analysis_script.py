# R2 analysis
from sklearn.linear_model import LinearRegression
od_pairs = df2.groupby([c2[1], c2[2]])
r2_vals = []
pos_slope = 0
valid = 0
for (o,d), g in od_pairs:
    if len(g) < 3:
        continue
    valid += 1
    gs = g.sort_values(c2[0])
    X = np.arange(len(gs)).reshape(-1,1)
    y = gs[c2[3]].values
    m = LinearRegression().fit(X, y)
    r2_vals.append(m.score(X, y))
    if m.coef_[0] > 0:
        pos_slope += 1
r2_arr = np.array(r2_vals)
daily_total = df2.groupby(c2[0])[c2[3]].sum()
X_d = np.arange(len(daily_total)).reshape(-1,1)
y_d = daily_total.values
m_d = LinearRegression().fit(X_d, y_d)

p(f"  OD对数量(>=3天数据): {valid}")
p(f"  R2 均值: {r2_arr.mean():.4f}")
p(f"  R2 中位数: {np.median(r2_arr):.4f}")
p(f"  R2 > 0.5: {(r2_arr>0.5).mean()*100:.1f}%")
p(f"  R2 > 0.8: {(r2_arr>0.8).mean()*100:.1f}%")
p(f"  R2 < 0.1: {(r2_arr<0.1).mean()*100:.1f}%")
p(f"  正趋势比例: {pos_slope/valid*100:.1f}%")
p(f"  日均总货量趋势R2: {m_d.score(X_d, y_d):.4f}")
